package com.fossaplayer;

public class Album {
    String album;
    String artist;
    String albumArt;
    String albumId;

    public String getAlbum() {
        return album;
    }

    public String getArtist() {
        return artist;
    }

    public String getAlbumArt() {
        return albumArt;
    }

    public String getAlbumId() {
        return albumId;
    }
}
